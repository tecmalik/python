
alphabet = input("Enter any alphabet : ")		
if alphabet in ['A','a', 'E', 'e','I', 'i', 'O', 'o', 'U' ,'u']:
	print (alphabet, " is a vowel")
elif alphabet in ['D','d', 'F','f', 'G','g', 'H','h', 'J','j','K','k' ,'L','l', 'M','m', 'P','p','Q','q' ,'R','r', 'S', 's', 'T', 't', 'U','u' ,'V','v' ,'W','w', 'X','x', 'Y','y','Z','z' ]:
	print(alphabet+" is a consonant")
	
else :
	print("Error  ..invalid Alphabet..")

		
